module ArithmeticKoans

function sum(x, y)
    return x + y
end

function difference(x, y)
    return x - y
end

function multiplication(x, y)
    return x * y
end

function division(x, y)
    return x / y
end

function integer_division(x, y)
    return div(x, y)
end

function power(x, y)
    return x^y
end

function modulo(x, y)
    return x % y
end

end
